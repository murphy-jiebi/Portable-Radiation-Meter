/*
*********************************************************************************************************
*	嚙踝蕭 嚙踝蕭 嚙踝蕭: EE_Delay
*	嚙踝蕭嚙踝蕭佽嚙踝蕭: 嚙踝蕭奀
*	嚙踝蕭    嚙踝蕭: 嚙踝蕭
*	嚙踝蕭 嚙踝蕭 硉: 嚙踝蕭
*********************************************************************************************************
*/
static void RTC_Delay(void)
{
	delay_us(4);
}


/*
*********************************************************************************************************
*	嚙踝蕭 嚙踝蕭 嚙踝蕭: EE_Start
*	嚙踝蕭嚙踝蕭佽嚙踝蕭: I2C 嚙踝蕭宎嚙褐綽蕭
*	嚙踝蕭    嚙踝蕭: 嚙踝蕭
*	嚙踝蕭 嚙踝蕭 硉: 嚙踝蕭
*********************************************************************************************************
*/
void RTC_Start(void)
{
	/* 嚙踝蕭 SCL 嚙賠蛛蕭ⅸ奀嚙踝蕭SDA 嚙踝蕭嚙踝蕭珨嚙踝蕭嚙踝蕭嚙踝蕭嚙諍梧蕭尨 I2C 嚙踝蕭嚙踝蕭嚙踝蕭宎嚙褐綽蕭 */
	SDA_HIGH();
	SCL_HIGH();
	RTC_Delay();
	SDA_LOW();
	RTC_Delay();	
	SCL_LOW();
	RTC_Delay();
}

/*
*********************************************************************************************************
*	嚙踝蕭 嚙踝蕭 嚙踝蕭: EE_Stop
*	嚙踝蕭嚙踝蕭佽嚙踝蕭: I2C 礿砦嚙褐綽蕭
*	嚙踝蕭    嚙踝蕭: 嚙踝蕭
*	嚙踝蕭 嚙踝蕭 硉: 嚙踝蕭
*********************************************************************************************************
*/
void RTC_Stop(void)
{
	/* 嚙踝蕭 SCL 嚙賠蛛蕭ⅸ奀嚙踝蕭SDA 嚙踝蕭嚙踝蕭珨嚙踝蕭嚙踝蕭嚙踝蕭嚙諍梧蕭尨 I2C 嚙踝蕭嚙賠踝蕭嚙踝蕭嚙褐綽蕭 */
	SDA_LOW();
	SCL_HIGH();
	RTC_Delay();
	SDA_HIGH();
	RTC_Delay();	
}


/*
*********************************************************************************************************
*	嚙踝蕭 嚙踝蕭 嚙踝蕭: EE_SendByte
*	嚙踝蕭嚙踝蕭佽嚙踝蕭: 嚙踝蕭 I2C 嚙踝蕭嚙賠瘀蕭嚙踝蕭珨嚙踝蕭嚙誰誹蛛蕭嚙踝蕭嚙踝蕭
*	嚙踝蕭    嚙踝蕭: _ucByte     猁嚙踝蕭嚙談蛛蕭嚙踝蕭嚙踝蕭
*	嚙踝蕭 嚙踝蕭 硉: 嚙踝蕭
*********************************************************************************************************
*/
void RTC_SendByte(uint8_t _ucByte)
{
	uint8_t i;

	/* 嚙褓瘀蕭嚙踝蕭嚙誰誹腔賂蕭弇 bit7 */
	for (i = 0; i < 8; i++)
	{
		if (_ucByte & 0x80)
		{
			SDA_HIGH();
		}
		else
		{
			SDA_LOW();
		}
		RTC_Delay();
		SCL_HIGH();
		RTC_Delay();
		SCL_LOW();
		if (i == 7)
		{
			SDA_HIGH(); /* 嚙談瘀蕭嚙踝蕭嚙踝蕭 */ 
		}
		_ucByte <<= 1;	/* 嚙踝蕭嚙踝蕭珨弇 */ 
		RTC_Delay();
	}
}


/*
*********************************************************************************************************
*	嚙踝蕭 嚙踝蕭 嚙踝蕭: EE_ReadByte
*	嚙踝蕭嚙踝蕭佽嚙踝蕭: 嚙踝蕭龰 I2C 嚙踝蕭嚙踝蕭嚙踝蕭珨嚙踝蕭嚙誰誹蛛蕭嚙踝蕭嚙踝蕭
*	嚙踝蕭    嚙踝蕭: 嚙踝蕭
*	嚙踝蕭 嚙踝蕭 硉: 嚙踝蕭龰嚙踝蕭嚙踝蕭嚙踝蕭嚙踝蕭
*********************************************************************************************************
*/
uint8_t RTC_ReadByte(void)
{
	uint8_t i;
	uint8_t ucByte = 0;

	/* 嚙踝蕭嚙踝蕭嚙踝蕭 1 嚙踝蕭 bit 峈嚙踝蕭嚙豎蛛蕭 bit7 */
	for (i = 0; i < 8; i++)
	{
		ucByte <<= 1;
		SCL_HIGH();
		RTC_Delay();
		if (SDA_READ())
		{
			ucByte++;
		}
		SCL_LOW();
		RTC_Delay();
	}
	return ucByte;
}



/*
*********************************************************************************************************
*	嚙踝蕭 嚙踝蕭 嚙踝蕭: EE_WaitAck
*	嚙踝蕭嚙踝蕭佽嚙踝蕭: 嚙褓湛蕭 I2C 嚙踝蕭嚙踝蕭茼嚙踝蕭嚙褐綽蕭
*	嚙踝蕭    嚙踝蕭: 嚙踝蕭
*	嚙踝蕭 嚙踝蕭 硉: 嚙踝蕭嚙踝蕭 1 嚙踝蕭嚙踝蕭茼嚙踢˙瘀蕭嚙踝蕭 0 嚙踝蕭茼嚙踝蕭
*********************************************************************************************************
*/
uint8_t RTC_WaitAck(void)
{
	uint8_t re;

	SDA_HIGH();	/* CPU 嚙談瘀蕭 SDA 嚙踝蕭 */
	RTC_Delay();
	SCL_HIGH();	/* CPU 嚙踝蕭嚙踝蕭 SCL = 1, 嚙踝蕭奀嚙踝蕭嚙踝蕭嚙賦殿嚙踝蕭 ACK 茼嚙踝蕭 */
	RTC_Delay();
	if(SDA_READ())
	{
		re = 1;
	}
	else
	{
		re = 0;
	}
	SCL_LOW();
	RTC_Delay();
	return re;
}


/*
*********************************************************************************************************
*	嚙踝蕭 嚙踝蕭 嚙踝蕭: EE_Ack
*	嚙踝蕭嚙踝蕭佽嚙踝蕭: MCU 嚙踝蕭嚙踝蕭珨嚙踝蕭茼嚙踝蕭嚙褐綽蕭
*	嚙踝蕭    嚙踝蕭: 嚙踝蕭
*	嚙踝蕭 嚙踝蕭 硉: 嚙踝蕭
*********************************************************************************************************
*/
void RTC_Ack(void)
{
	SDA_LOW();	/* CPU 嚙踝蕭嚙踝蕭 SDA = 0 */
	RTC_Delay();
	SCL_HIGH();	/* CPU 嚙踝蕭嚙踝蕭 1 嚙踝蕭奀嚙踝蕭 */
	RTC_Delay();
	SCL_LOW();
	RTC_Delay();
	SDA_HIGH();	/* CPU 嚙談瘀蕭 SDA 嚙踝蕭嚙踝蕭 */	
}

/*
*********************************************************************************************************
*	嚙踝蕭 嚙踝蕭 嚙踝蕭: EE_NAck
*	嚙踝蕭嚙踝蕭佽嚙踝蕭: CPU 嚙踝蕭嚙踝蕭嚙踝蕭茼嚙踝蕭嚙褐綽蕭
*	嚙踝蕭    嚙踝蕭: 嚙踝蕭
*	嚙踝蕭 嚙踝蕭 硉: 嚙踝蕭
*********************************************************************************************************
*/
void RTC_NAck(void)
{
	SDA_HIGH();	/* CPU 嚙踝蕭嚙踝蕭 SDA = 1 */
	RTC_Delay();
	SCL_HIGH();	/* CPU 嚙踝蕭嚙踝蕭 1 嚙踝蕭奀嚙踝蕭 */
	RTC_Delay();
	SCL_LOW();
	RTC_Delay();
}



/*
*********************************************************************************************************
*	嚙踝蕭 嚙踝蕭 嚙踝蕭: read_eeprom
*	嚙踝蕭嚙踝蕭佽嚙踝蕭: EEPROM嚙踝蕭龰嚙踝蕭嚙踝蕭
*	嚙踝蕭    嚙踝蕭: 
*	嚙踝蕭 嚙踝蕭 硉: 
*********************************************************************************************************
*/
void RTC_Write(uint8_t _ucAddr, uint8_t *_ucpData, uint8_t _ucLen)
{
	RTC_Start();
	
	RTC_SendByte(BL5372T_WR);	/* 迡嚙質掘嚙踝蕭硊 */
	
	if(RTC_WaitAck() != 0)
	{
		goto cmd_fail;			
	}
	
	RTC_SendByte(_ucAddr << 4);	/* 迡嚙衝湛蕭嚙踝蕭嚙踝蕭硊 */
	
	if(RTC_WaitAck() != 0)
	{
		goto cmd_fail;			
	}
	
	while(_ucLen--)
	{
		RTC_SendByte(*_ucpData++);	/* 迡嚙衝湛蕭嚙踝蕭嚙踝蕭硊 */
		
		if(RTC_WaitAck() != 0)
		{
			goto cmd_fail;			
		}		
	}
	
cmd_fail:
	RTC_Stop();	
}

/*
*********************************************************************************************************
*	嚙踝蕭 嚙踝蕭 嚙踝蕭: read_eeprom
*	嚙踝蕭嚙踝蕭佽嚙踝蕭: EEPROM嚙踝蕭龰嚙踝蕭嚙踝蕭
*	嚙踝蕭    嚙踝蕭: 
*	嚙踝蕭 嚙踝蕭 硉: 
*********************************************************************************************************
*/
void RTC_Read(uint8_t _ucAddr, uint8_t *_ucpData, uint8_t _ucLen)
{
	RTC_Start();
	
	RTC_SendByte(BL5372T_WR);	/* 迡嚙質掘嚙踝蕭硊 */
	
	if(RTC_WaitAck() != 0)
	{
		goto cmd_fail;			
	}
	
	RTC_SendByte(_ucAddr << 0x04);	/* 迡嚙衝湛蕭嚙踝蕭嚙踝蕭硊 */
	
	if(RTC_WaitAck() != 0)
	{
		goto cmd_fail;			
	}

	RTC_Start();
	
	RTC_SendByte(BL5372T_RD);	/* 迡嚙質掘嚙踝蕭硊 */
	
	if(RTC_WaitAck() != 0)
	{
		goto cmd_fail;			
	}
	
	while(_ucLen--)
	{
		*_ucpData++ = RTC_ReadByte();
		
		if (_ucLen != 0)          /* 茼嚙踝蕭嚙褐綽蕭 */
		{
			RTC_Ack();	       
		}	
		else
		{
			RTC_NAck();
		}			
	}
	
cmd_fail:
	RTC_Stop();	
}
/*
*********************************************************************************************************
*	嚙踝蕭 嚙踝蕭 嚙踝蕭: read_eeprom
*	嚙踝蕭嚙踝蕭佽嚙踝蕭: EEPROM嚙踝蕭龰嚙踝蕭嚙踝蕭
*	嚙踝蕭    嚙踝蕭: 
*	嚙踝蕭 嚙踝蕭 硉: 
*********************************************************************************************************
*/
uint8_t Get_RTC_Time(uint8_t *_ucpTime)
{
	uint8_t s[7];
	
	RTC_Read(0x00, s, 7);
	
	_ucpTime[_ss] = Byte_BCDToBin(s[0]);
	_ucpTime[_mm] = Byte_BCDToBin(s[1]);
	_ucpTime[_hh] = Byte_BCDToBin(s[2]);
	_ucpTime[_dd] = Byte_BCDToBin(s[4]);
	_ucpTime[_mon] = Byte_BCDToBin(s[5]);
	_ucpTime[_yy] = Byte_BCDToBin(s[6]);
	_ucpTime[_ww] = Byte_BCDToBin(s[3]);
	
	return _TRUE;
}


/*
*********************************************************************************************************
*	嚙踝蕭 嚙踝蕭 嚙踝蕭: Set_RTC_Time
*	嚙踝蕭嚙踝蕭佽嚙踝蕭: EEPROM嚙踝蕭龰嚙踝蕭嚙踝蕭
*	嚙踝蕭    嚙踝蕭: 
*	嚙踝蕭 嚙踝蕭 硉: 
*********************************************************************************************************
*/
uint8_t Set_RTC_Time(uint8_t *_ucpTime)
{
	uint8_t s[7];
	
	s[0] = Byte_BinToBCD(_ucpTime[_ss]);
	s[1] = Byte_BinToBCD(_ucpTime[_mm]);
	s[2] = Byte_BinToBCD(_ucpTime[_hh]);
	RTC_Write(0x00, s, 3);
	delay_ms(5);
	
	s[4] = Byte_BinToBCD(_ucpTime[_dd]);
	s[5] = Byte_BinToBCD(_ucpTime[_mon]);
	s[6] = Byte_BinToBCD(_ucpTime[_yy]);
	RTC_Write(0x04, s + 4, 3);

    return _TRUE;
}






/*
*********************************************************************************************************
*	嚙踝蕭 嚙踝蕭 嚙踝蕭: bsp_EEPROMInit
*	嚙踝蕭嚙踝蕭佽嚙踝蕭: 嚙踝蕭宎嚙踝蕭嚙踝蕭嚙踝蕭嚙踝蕭嚙褐對蕭
*	嚙踝蕭    嚙踝蕭: 嚙踝蕭*	嚙踝蕭 嚙踝蕭 硉: 嚙踝蕭
*********************************************************************************************************
*/
void BSP_RTC_Init(void)
{
	uint8_t ucData = 0x00;
	
	rcu_periph_clock_enable(RCU_GPIOB);
	
	gpio_init(BL_GPIO_PORT, GPIO_MODE_OUT_OD, GPIO_OSPEED_50MHZ, SCL_PIN | SDA_PIN);
		
	RTC_Stop();

	ucData = 0x01;
	RTC_Write(0x0E, &ucData, 1);
	ucData = 0x2C;
	RTC_Write(0x0F, &ucData, 1);

}









