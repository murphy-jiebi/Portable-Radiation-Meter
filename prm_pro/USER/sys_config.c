#include "bsp.h"
/*********************************************************************************************************
*                                            变量声明
*********************************************************************************************************/
uint32_t g_taskTicks=0;

/*********************************************************************************************************
*	函 数 名: getTaskTicks
*	功能说明: 
*	形    参: 无
*	返 回 值: 无
*********************************************************************************************************/
void setSystemTick(uint32_t ms)
{ 
    if(SysTick_Config((SystemCoreClock /1000U)*ms)) 
	{
        while(1);
    }
    /* 配置中断优先级分组 */
    NVIC_SetPriority(SysTick_IRQn, 0x00U);	
}

/*********************************************************************************************************
*	函 数 名: getTaskTicks
*	功能说明: 
*	形    参: 无
*	返 回 值: 无
*********************************************************************************************************/
uint32_t getTaskTicks(void)
{
    return g_taskTicks;
}

/*********************************************************************************************************
*	函 数 名: taskTicksInc
*	功能说明: 
*	形    参: 无
*	返 回 值: 无
*********************************************************************************************************/
void taskTicksInc(void) // 时间基准
{
    g_taskTicks++;
}

/*********************************************************************************************************
*	函 数 名: taskTickDiff
*	功能说明: 
*	形    参: 无
*	返 回 值: 无
*********************************************************************************************************/
uint32_t taskTickDiff(uint32_t now,uint32_t last)
{
    uint64_t diff;
    diff = now + 0x100000000 - last;

    return (diff & 0xffffffff);
}


