#define BL5372T_RD			0X65			/* 嚙踝蕭嚙踝蕭硊 */
#define BL5372T_WR			0X64			/* 迡嚙踝蕭硊 */


/* 嚙踝蕭嚙踝蕭EEPROM嚙踝蕭嚙踝蕭GPIO嚙誼選蕭*/
#define BL_GPIO_PORT	GPIOB				/* GPIO嚙誼選蕭 */
#define SCL_PIN		    GPIO_PIN_6			/* 嚙踝蕭嚙諉蛛蕭SCL奀嚙踝蕭嚙賠蛛蕭GPIO */
#define SDA_PIN		    GPIO_PIN_7		
/* 嚙踝蕭嚙諉蛛蕭SDA嚙踝蕭嚙踝蕭嚙賠蛛蕭GPIO */

#define SCL_HIGH()   GPIO_BOP(BL_GPIO_PORT) = (uint32_t)SCL_PIN				/* SCL = 1 */
#define SCL_LOW()    GPIO_BC(BL_GPIO_PORT) = (uint32_t)SCL_PIN				/* SCL = 0 */

#define SDA_HIGH()   GPIO_BOP(BL_GPIO_PORT) = (uint32_t)SDA_PIN				/* SDA = 1 */
#define SDA_LOW()    GPIO_BC(BL_GPIO_PORT) = (uint32_t)SDA_PIN				/* SDA = 0 */

#define SDA_READ()   ((uint32_t)RESET != (GPIO_ISTAT(BL_GPIO_PORT) & (SDA_PIN)))		/* 嚙踝蕭SDA嚙誼選蕭袨怓 */


